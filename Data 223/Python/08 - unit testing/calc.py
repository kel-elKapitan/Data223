
# A simple calculator class

class Calculator():
    '''
    A simple calculator class
    '''

    def __init__(self):
        pass


    def add(self,a,b):
        '''
        Function to add two numbers
        '''
        return a + b

    def subtract(self,a,b):
        '''
        Subtract two numbers
        '''
        return a - b

    def multiply(self,a,b):
        '''
        Multiply two numbers
        '''
        return a * b

    def divide(self,a,b):
        '''
        Function to divide two numbers
        '''
        return a / b